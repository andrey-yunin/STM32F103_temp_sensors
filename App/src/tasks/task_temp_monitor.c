#include "task_temp_monitor.h"
#include "cmsis_os.h"       // Для osDelay, osMessageQueueXxx
#include "app_globals.h"
#include "ds18b20.h"

/**
 * @brief Функция, реализующая задачу мониторинга температуры.
 *        Эта задача периодически опрашивает датчики DS18B20,
 *        усредняет показания и сохраняет их в глобальный массив.
 * @param argument: Не используется.
 */

void app_start_task_temp_monitor(void *argument)
{
	// --- Инициализация DS18B20 ---
	// Находим все датчики на шине при старте.
	uint8_t found_sensors = DS18B20_Init();
	// Заглушка для отладки: если датчики найдены, можно что-то вывести.
	// В будущем здесь будет проверка, если не найдено достаточно датчиков.

	// --- Основной цикл задачи ---
	for(;;)
		{
		// Пока что просто заглушка: запускаем измерение и ждем.
	    // Полная логика опроса, усреднения и сохранения будет добавлена позже.
	    DS18B20_StartAll(); // Запустить измерение на всех датчиках
	    osDelay(750);      // Ждем, пока датчики измерят температуру (макс. 750 мс)

	    // Здесь в будущем будет логика чтения и усреднения для каждого датчика.
	    // Пока просто имитация.
	    for (uint8_t i = 0; i < found_sensors; i++) {
	    	g_latest_temperatures[i] = 25.0f + (float)i/10.0f; // Имитация температуры
	    	}
	    osDelay(5000); // Опрашиваем датчики каждые 5 секунд
	    }
 }
